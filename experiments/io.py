from __future__ import annotations

import argparse
import datetime as dt
import hashlib
import json
from pathlib import Path
from typing import Any

import pandas as pd
import yaml


def load_yaml_config(path: str | Path) -> dict[str, Any]:
    config_path = Path(path)
    with config_path.open("r", encoding="utf-8") as fh:
        return yaml.safe_load(fh)


def load_json(path: str | Path) -> dict[str, Any]:
    in_path = Path(path)
    with in_path.open("r", encoding="utf-8") as fh:
        return json.load(fh)


def save_json(path: str | Path, payload: dict[str, Any]) -> None:
    out_path = Path(path)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    with out_path.open("w", encoding="utf-8") as fh:
        json.dump(payload, fh, indent=2, sort_keys=True)


def save_csv(path: str | Path, df: pd.DataFrame) -> None:
    out_path = Path(path)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    df.to_csv(out_path, index=False)


def ensure_dir(path: str | Path) -> Path:
    out_path = Path(path)
    out_path.mkdir(parents=True, exist_ok=True)
    return out_path


def stable_config_hash(config: dict[str, Any]) -> str:
    serialized = json.dumps(config, sort_keys=True, separators=(",", ":")).encode("utf-8")
    return hashlib.sha256(serialized).hexdigest()


def make_run_id(config_hash: str, now_utc: dt.datetime | None = None) -> str:
    ts = (now_utc or dt.datetime.now(dt.timezone.utc)).strftime("%Y%m%dT%H%M%SZ")
    return f"{ts}-{config_hash[:8]}"


def derive_legacy_run_id(created_at_utc: str, config_hash: str) -> str:
    material = f"{created_at_utc}|{config_hash}".encode("utf-8")
    digest = hashlib.sha256(material).hexdigest()[:8]
    return f"legacy-{digest}"


